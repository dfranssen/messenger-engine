package com.ditavision.messengerengine.mmp.request;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * 
 * @author Dirk Franssen
 */
@XmlRootElement(name = "authentication")
@XmlAccessorType(XmlAccessType.FIELD)
public class MMPAuthentication {
    private String username;
    private String password;

    public MMPAuthentication() {
    }

    public MMPAuthentication(String username, String password) {
        this.username = username;
        this.password = password;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    @Override
    public String toString() {
        return "MMPAuthentication{" + "username=" + username + ", password=" + password + '}';
    }
}
